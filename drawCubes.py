# done in trinket

import turtle
t =turtle.Turtle()
def maker(t):
  t.forward(30)
  t.left(120)
  t.forward(30)
  t.left(60)
  t.forward(30)
  t.left(120)
  t.forward(30)
def makecube(t):
  t.left(30)
  t.fillcolor('Light Blue')
  t.begin_fill()
  maker(t)
  t.end_fill()
  t.fillcolor("dark blue")
  t.begin_fill()
  t.right(60)
  maker(t)
  t.end_fill()
  t.fillcolor('blue')
  t.begin_fill()
  t.right(60)
  maker(t)
  t.end_fill()

t.speed(20)
makecube(t)
t.penup()
t.right(90)
t.forward(50)
t.pendown()
makecube(t)
t.left(60)
t.forward(30)
t.right(60)
t.penup()
t.forward(30)
t.pendown()
t.right(90)

